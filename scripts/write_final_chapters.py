def count_chinese(text):
    return len(re.findall(r'[\u4e00-\u9fff\u3400-\u4dbf]', text))

def write_chapter(num, title, content):
    fn = f"{CHAPTERS_DIR}/第{num:02d}章_{title}.txt"
    with open(fn, 'w', encoding='utf-8') as f:
        f.write(content)
    cc = count_chinese(content)
    print(f"  Ch{num:02d} {title}: {cc} Chinese chars {'✅' if 2000 <= cc <= 3500 else '❌'}")
    return cc, fn

def post_chapter(num, fn):
    r = subprocess.run(
        ["python", "novel.py", "post", str(num), "--story", "--no-jury",
         "--file", f"workspace/slot_006/chapters/{os.path.basename(fn)}"],
        capture_output=True, text=True, timeout=60)
    out = r.stdout + r.stderr
    if "全部门禁通过" in out:
        print(f"    post: ✅ PASS")
        return True
    elif "OK" in out and "字数" in out:
        print(f"    post: ✅ OK")
        return True
    else:
        # Show error
        err_lines = [l for l in out.split('\n') if 'ERROR' in l or 'error' in l or '❌' in l or 'FAIL' in l]
        for l in err_lines[:3]:
            print(f"    {l.strip()}")
        print(f"    post: ⚠️  check output")
        return False

